import Foundation

enum TokenType
{
    case leftParenthesis
    case rightParenthesis
    case comma
    case dot
    case minus
    case plus
    case semicolon
    case colon
    case slash
    case star
    case bang
    case bangEqual
    case equal
    case equalEqual
    case greater
    case greaterEqual
    case less
    case lessEqual
    case identifier
    case number
    case print
    case and
    case or
    case `false`
    case `true`
    case EOF
}
